SCORED = ('hit', 'correct', 'near', 'miss', 'incorrect')

SQL = """
UPDATE predictions
SET outcome = 'unresolved',
    evaluation_summary = 'Note-as-quote sweep 2026-06-12 -> unresolved. [pre_remediation] '
        || json_build_object('tag', %(tag)s, 'outcome', outcome,
                             'actual_return', actual_return,
                             'evaluation_date', evaluation_date)::text
        || ' | prior: ' || COALESCE(evaluation_summary, '(none)'),
    evaluated_at = CASE WHEN outcome IN %(scored)s THEN NOW() ELSE evaluated_at END
WHERE id = ANY(%(ids)s)
  AND (outcome IN %(scored)s OR outcome = 'pending' OR outcome IS NULL)
  AND COALESCE(evaluation_summary, '') NOT LIKE '%%[pre_remediation]%%'
"""


def main():
    conn = psycopg2.connect(os.environ['DATABASE_PUBLIC_URL'])
    cur = conn.cursor()
    total = 0
    for tag, ids in (('judged_bad_2026_06_12', JUDGED_BAD),
                     ('unverified_quote_2026_06_12', UNVERIFIED_QUOTE)):
        cur.execute(SQL, {'tag': tag, 'ids': ids, 'scored': SCORED})
        print(f'{tag}: {cur.rowcount} rows re-marked (of {len(ids)} pinned)')
        total += cur.rowcount
    conn.commit()
    cur.execute("SELECT count(*) FROM predictions WHERE outcome='unresolved' "
                "AND evaluation_summary LIKE '%%[pre_remediation]%%' "
                "AND (evaluation_summary LIKE '%%judged_bad_2026_06_12%%' "
                "     OR evaluation_summary LIKE '%%unverified_quote_2026_06_12%%')")
    print(f'total re-marked this run: {total}; cohort rows now tagged: {cur.fetchone()[0]}')
    conn.close()


if __name__ == '__main__':
    main()
