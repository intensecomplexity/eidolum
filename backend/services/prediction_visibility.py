"""Shared filters for prediction queries.

This module exposes THREE filters with very different policies. Mixing
them up was a bug — `not_excluded_filter` and `non_qwen_filter` were
treated as user-visibility filters until 2026-05-27 and quietly hid
hundreds of thousands of legitimate Wall St rating rows from every
user-facing surface. The distinction now:

  yt_visible_filter   — USER VISIBILITY. Hides legacy YouTube rows
                        whose source_timestamp_seconds never got
                        resolved by the backfill worker. Apply on
                        every user-facing query.

  not_excluded_filter — TRAINING-DATA ONLY. The audit that sets
                        excluded_from_training=TRUE (ship_12_audit.py
                        v12.3, sonnet_rules_v2_mis quarantine, etc.)
                        is meant to keep the fine-tune loader from
                        seeing rows with synthetic/blanket-stamped
                        fields. The flagged rows are still real
                        predictions with real outcomes and SHOULD
                        appear in user-facing leaderboards, feeds,
                        and stats. Apply only inside training-data
                        loaders. No such loader currently lives in
                        this repo, so this helper currently has no
                        callers.

  non_qwen_filter     — TRAINING-DATA ONLY. Same rationale: the Qwen
                        LoRA rows from the 2026-04-19 audit phase
                        should be excluded from fine-tune training,
                        not from user views. The 192 scored Qwen
                        predictions are real and should count. Apply
                        only inside training-data loaders.

Historical note: the 2026-04-18 policy block at the top of this file
previously said "every user-facing query" pairs all three filters.
That was the bug — only yt_visible_filter belonged in that bucket.
"""
from __future__ import annotations


def yt_visible_filter(alias: str = "p") -> str:
    """Return a SQL fragment that excludes legacy YouTube predictions
    with NULL source_timestamp_seconds. Safe to paste into any WHERE
    clause with a leading ``AND``.

    Shape:
        NOT (<alias>.source_type = 'youtube'
             AND <alias>.source_timestamp_seconds IS NULL)

    Non-YouTube rows pass regardless. YouTube rows with a real
    timestamp pass. YouTube rows with NULL timestamp are excluded.
    """
    prefix = f"{alias}." if alias else ""
    return (
        f"NOT ({prefix}source_type = 'youtube' "
        f"AND {prefix}source_timestamp_seconds IS NULL)"
    )


def not_excluded_filter(alias: str = "p") -> str:
    """TRAINING-DATA ONLY. Drop rows flagged `excluded_from_training`.

    Ship #12 (v12.3 invented_timeframe + later sonnet_rules_v2_mis
    quarantine, screencast_cull, etc.) flags rows so the fine-tune
    loader doesn't see synthetic-template or quality-suspect inputs.
    The flagged rows are still real predictions with real outcomes
    and MUST appear on user-facing leaderboards, feeds, and stats —
    do NOT use this filter in user-facing queries.

    Shape:
        COALESCE(<alias>.excluded_from_training, FALSE) = FALSE
    """
    prefix = f"{alias}." if alias else ""
    return f"COALESCE({prefix}excluded_from_training, FALSE) = FALSE"


def non_qwen_filter(alias: str = "p") -> str:
    """TRAINING-DATA ONLY. Drop YouTube rows generated by Qwen LoRA.

    Originated in the 2026-04-19 Haiku-vs-Qwen audit phase: Qwen rows
    were kept out of fine-tune corpora until the per-model accuracy
    audit completed. They are otherwise valid predictions and should
    appear on user surfaces — do NOT use this filter in user-facing
    queries.

    Shape:
        COALESCE(<alias>.generating_model, '') != 'qwen_lora_v1'
    """
    prefix = f"{alias}." if alias else ""
    return f"COALESCE({prefix}generating_model, '') != 'qwen_lora_v1'"


# Bare-table forms for queries that don't use an alias
YT_VISIBLE_FILTER_SQL = yt_visible_filter("")
NON_QWEN_FILTER_SQL = non_qwen_filter("")
NOT_EXCLUDED_FILTER_SQL = not_excluded_filter("")
